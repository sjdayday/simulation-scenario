package org.grayleaves.utility;

public class UnsupportedParameterException extends Exception
{
	private static final long serialVersionUID = 1L;

	public UnsupportedParameterException()
	{
	}

	public UnsupportedParameterException(String arg0)
	{
		super(arg0);

	}

	public UnsupportedParameterException(Throwable arg0)
	{
		super(arg0);

	}

	public UnsupportedParameterException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);

	}

}
