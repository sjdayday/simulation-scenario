package org.grayleaves.utility;

public class OutputFileBuilderException extends Exception
{

	private static final long serialVersionUID = 1L;

	public OutputFileBuilderException()
	{
	}

	public OutputFileBuilderException(String message)
	{
		super(message);

	}

	public OutputFileBuilderException(Throwable cause)
	{
		super(cause);

	}

	public OutputFileBuilderException(String message, Throwable cause)
	{
		super(message, cause);

	}

}
