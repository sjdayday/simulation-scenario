package org.grayleaves.utility;

public class ScenarioException extends Exception
{

	private static final long serialVersionUID = 1L;

	public ScenarioException()
	{
	}

	public ScenarioException(String arg0)
	{
		super(arg0);

	}

	public ScenarioException(Throwable arg0)
	{
		super(arg0);

	}

	public ScenarioException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);

	}

}
