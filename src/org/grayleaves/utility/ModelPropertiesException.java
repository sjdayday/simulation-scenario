package org.grayleaves.utility;

public class ModelPropertiesException extends Exception
{

	private static final long serialVersionUID = 1L;

	public ModelPropertiesException()
	{
	}

	public ModelPropertiesException(String arg0)
	{
		super(arg0);

	}

	public ModelPropertiesException(Throwable arg0)
	{
		super(arg0);

	}

	public ModelPropertiesException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);

	}

}
