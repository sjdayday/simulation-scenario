package org.grayleaves.utility;

public class ArrayLengthMismatchException extends Exception
{

	private static final long serialVersionUID = 2027147963656200215L;

	public ArrayLengthMismatchException()
	{
	}

	public ArrayLengthMismatchException(String arg0)
	{
		super(arg0);

	}

	public ArrayLengthMismatchException(Throwable arg0)
	{
		super(arg0);

	}

	public ArrayLengthMismatchException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);

	}

}
